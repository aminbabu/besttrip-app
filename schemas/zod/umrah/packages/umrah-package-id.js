// dependencies
const { z } = require('zod');
const { isMongoId } = require('validator');

// export umrah package id schema
module.exports = z
    .object({
        id: z
            .string({
                required_error: 'Id is required',
                invalid_type_error: 'Please provide a valid id',
            })
            .refine((id) => isMongoId(id), {
                message: 'Please provide a valid id',
            }),
    })
    .strict();
